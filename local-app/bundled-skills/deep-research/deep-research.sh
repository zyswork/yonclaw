#!/bin/bash
TOPIC="$1"
cat <<EOF
请对以下主题进行深度研究：

**主题**: $TOPIC

## 研究步骤

1. **分解子问题**: 将主题分解为 3-5 个具体的子问题
2. **信息收集**: 对每个子问题使用 web_search 工具搜索最新信息
3. **深度分析**: 使用 web_fetch 获取关键页面的详细内容
4. **综合报告**: 基于所有收集的信息，生成结构化的研究报告

## 输出要求

生成 Markdown 格式的报告，包含：
- **摘要**（200 字以内）
- **背景与现状**
- **关键发现**（每个子问题一节）
- **数据与证据**
- **结论与建议**
- **参考来源**

请现在开始研究。
EOF
